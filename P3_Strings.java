public class P3_Strings {
    
    public static void main(String[] args) {

        String s = "Hello!";

        System.out.println(s);

        System.out.println(s + s);

        System.out.println("Length: " + s.length());

        System.out.println("First letter: " + s.charAt(0));

        int i = 1;

        System.out.println(s + i);

    }
}